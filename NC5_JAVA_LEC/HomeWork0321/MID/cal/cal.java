package HomeWork0321.MID.cal;

public class cal {
    public class Add {
        public int add(int a, int b) {
            return a + b;
        }
    }

    public class Sub {
        public int sub(int a, int b) {
            return a - b;
        }
    }

    public class Mul {
        public int mul(int a, int b) {
            return a * b;
        }
    }

    public class Div {

        public int div(int a, int b) {
            return a / b;
        }
    }

}
